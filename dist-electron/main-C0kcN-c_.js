async function fetchSummonerName(credentials) {
  const { port, password } = credentials;
  process.env.NODE_TLS_REJECT_UNAUTHORIZED = "0";
  try {
    const response = await fetch(
      `https://127.0.0.1:${port}/lol-summoner/v1/current-summoner`,
      {
        method: "GET",
        headers: {
          Authorization: `Basic ${Buffer.from(`riot:${password}`).toString("base64")}`,
          Accept: "application/json"
        }
      }
    );
    if (response.ok) {
      const data = await response.json();
      if (data && data.gameName) {
        console.log("Summoner name fetched (gameName):", data.gameName);
        currentSummonerResponse = data;
        return data;
      } else {
        console.error("Neither gameName nor displayName found in the API response");
        return null;
      }
    } else {
      console.error(`HTTP error fetching summoner name: ${response.status}`);
      return null;
    }
  } catch (error) {
    console.error("Error fetching summoner name:", error);
    return null;
  } finally {
    process.env.NODE_TLS_REJECT_UNAUTHORIZED = "1";
  }
}
function createMainWindow() {
  const primaryDisplay = screen.getPrimaryDisplay();
  const allDisplays = screen.getAllDisplays();
  const externalDisplay = allDisplays.find(
    (display) => display.bounds.x > primaryDisplay.bounds.x
  );
  let x2 = 0, y = 0;
  if (externalDisplay) {
    x2 = externalDisplay.bounds.x;
    y = externalDisplay.bounds.y + 50;
  }
  debug("Creating main window");
  exports.mainWindow = new BrowserWindow({
    x: x2,
    y,
    minWidth: 1850,
    // set the minimum width 1600
    minHeight: 900,
    // set the minimum height 800
    width: 2560,
    height: 1240,
    // maxHeight: 800,
    // maxWidth: 1280,
    partition: "nopersist",
    webPreferences: {
      preload: path.join(__dirname, "preload.js"),
      contextIsolation: true,
      // Keep it true for security
      nodeIntegration: false
      // Node integration should be false in renderer for security
    }
  });
  if (process.env.NODE_ENV === "DEVELOPMENT") {
    exports.mainWindow.webContents.openDevTools();
  }
  debug("Loading main window");
  if (MAIN_WINDOW_VITE_DEV_SERVER_URL) {
    exports.mainWindow.loadURL(MAIN_WINDOW_VITE_DEV_SERVER_URL);
  } else {
    log.info(
      "Loading main window from file",
      path.join(__dirname, `../renderer/${MAIN_WINDOW_VITE_NAME}/index.html`)
    );
    exports.mainWindow.loadFile(
      path.join(__dirname, `../renderer/${MAIN_WINDOW_VITE_NAME}/index.html`)
    );
  }
  exports.mainWindow.once("ready-to-show", () => {
    mainWindowReady = true;
    isAppStartup = false;
  });
}
